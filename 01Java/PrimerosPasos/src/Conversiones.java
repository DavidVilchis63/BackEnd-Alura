
public class Conversiones {
	public static void main(String[] args) {
		
		double variable1 = 230.09;
		int variable1Entero = (int) variable1;
		
		System.out.println(variable1Entero);
		
		// Long soporta mayor cantidad de datos
		long prueba = 1222222122222222222L;
		System.out.println(prueba);
		
		// Short soporta numeros pequeños
		short numeroPequeno = 12333;
		System.out.println(numeroPequeno);
		
		// Byte soporta solo 8 byts de tamaño
		byte numeroMasPequeño = 122;
		System.out.println(numeroMasPequeño);
		
		// Float tambien soporta decimales pero pequeños
		float numeroDecimalPeqieno = 22.5F;
		System.out.println(numeroDecimalPeqieno);
		
	}

}
